import numpy as np
from sympy import *
import math
import random
import matplotlib.pyplot as plt

def restrict(restrictions: list,func: str):
    """
    Переписывает ограничения для решения задачи. 
    Parameters
    ----------
    restrictions: list
        Ограничения
    function: str
        Функция 
       
    Returns
    -------
    A: np.array
        Массив значений для ограничений
    B: np.array
        Массив ограничений
    C: np.array
        Массив значений функции    
    sign:
        Знак


    """
    sign = []
    C = []
    for i in func.split('x')[:-1]:
      C.append(int(i.split()[-1]))
    C = np.array(C, dtype=np.float)    

    A = []
    B = []

    for i in restrictions:
      function = str(i)

      if function.find('<=') != -1:
          sign.append('le')
          left, right = function.split('<=')
          B.append(int(right))
          a1 = []
          for i in left.split('x')[:-1]:
            a1.append(int(i.split()[-1]))
          A.append(a1)          

      elif function.find('>=') != -1:
          sign.append('me')   
          left, right = function.split('>=')
          B.append(int(right))
          a1 = []
          for i in left.split('x')[:-1]:
            a1.append(int(i.split()[-1]))
          A.append(a1)

      elif function.find('>') != -1:
          sign.append('m')   
          left, right = function.split('>')
          B.append(int(right))
          a1 = []
          for i in left.split('x')[:-1]:
            a1.append(int(i.split()[-1]))
          A.append(a1)

      elif function.find('<') != -1:
          sign.append('l')   
          left, right = function.split('<')
          B.append(int(right))
          a1 = []
          for i in left.split('x')[:-1]:
            a1.append(int(i.split()[-1]))
          A.append(a1)

      elif function.find('=') != -1:
          sign.append('e')   
          left, right = function.split('=')
          B.append(int(right))
          a1 = []
          for i in left.split('x')[:-1]:
            a1.append(int(i.split()[-1]))
          A.append(a1)
    A = np.array(A, dtype=np.float)
    B = np.array(B, dtype=np.float)


    return A,B,C,sign



